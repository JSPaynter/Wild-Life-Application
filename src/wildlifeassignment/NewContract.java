    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Boolean check = false, dateCheck = false;
        Date start, end;
        double salary; 
            
        try {
            check = Utilities.NullCheck(txtStart, "Start date of the contract", "Start Date") &&
                    Utilities.NullCheck(txtEnd, "End date of the contract", "End Date") &&
                    Utilities.NullCheck(txtSalary, "Salary", "Salary") &&
                    Utilities.CheckBefore(Utilities.todaysDate() ,Utilities.DateFormat().parse(txtStart.getText())) &&
                    Utilities.CheckBefore(Utilities.DateFormat().parse(txtStart.getText()), Utilities.DateFormat().parse(txtEnd.getText()));
                    
        } catch (ParseException ex) {}
        
                if (check && dateCheck) {
                    try {
                        start = Utilities.DateFormat().parse(txtStart.getText());
                        end = Utilities.DateFormat().parse(txtEnd.getText());
                        salary = Integer.parseInt(txtSalary.getText());
                        
                        proOb.setContractStart(start);
                        proOb.setContractEnd(end);
                        proOb.setAnnualSalary(salary);
                        
                        BWA.observers.remove(index);
                        BWA.observers.add(index, proOb);
                        
                        dispose();
                        Utilities.InfoBox("Contract successfully issued to " + proOb.getName(), "Contract Issued");
                        
                    } catch (ParseException ex) {}
                    
                }

    }//GEN-LAST:event_btnAddActionPerformed

