    private void btnViewedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewedActionPerformed
        int index = lstObservations.getSelectedIndex();
        if (index != -1) {
            if (JOptionPane.showConfirmDialog(null, "Are you sure you wish to remove this observation?", "Request", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)
                    == JOptionPane.YES_OPTION) {
                BWA.recentObservations.remove(index-1);
                PopulateList();
                ClearForm();
            }
        }
        else {
            Utilities.InfoBox( "Please select a observation from the list", "observation list");
        }            
    }//GEN-LAST:event_btnViewedActionPerformed

    private void SetForm(Observation o) {
        txtAnimal.setText(o.getAnimal().getName());
        txtObserver.setText(o.getObserver().getName());
        txtDate.setText(Utilities.DateFormat().format(o.getDate()));
        txtLocation.setText(o.getLocation());
        txtConfidence.setText(o.getConfidence());
        txtNoObservations.setText(Integer.toString(o.getNoOfObs()));
        txtDescription.setText(o.getDescription());
    }
    
    private void ClearForm() {
        txtAnimal.setText(null);
        txtObserver.setText(null);
        txtDate.setText(null);
        txtLocation.setText(null);
        txtConfidence.setText(null);
        txtNoObservations.setText(null);
        txtDescription.setText(null);        
    }
    
    private void lstObservationsClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstObservationsClicked
        int index = lstObservations.getSelectedIndex();
        if (BWA.recentObservations.isEmpty()) {        
        }
        else {
            if (index == 0) {
                index = 1;
                lstObservations.setSelectedIndex(1);
            }
            if (index != -1) {
                Observation observation = BWA.recentObservations.get(index-1);
                SetForm(observation);
            }
        }
    }//GEN-LAST:event_lstObservationsClicked

