    private void PopulateList() {
        
        ArrayList<String> observations = new ArrayList<>();
        if (BWA.recentObservations.isEmpty()) {
            observations.add("No recent observations");
        }
        else {
            observations.add("Recent Observations");
            for (Observation o : BWA.recentObservations) {
                String item = o.getAnimal().getName();
                String date = Utilities.DateFormat().format(o.getDate());
                item += " - " + date;
                observations.add(item);
            }
        }
        lstObservations.setListData(observations.toArray());
    }
    
    
